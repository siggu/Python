"""
1자리 : 1
2자리 : 10
3자리 : 101, 100
4자리 : 1000, 1010, 1001
5자리 : 10000, 10100, 10101, 10010, 10001
6자리 : 100000, 101000, 100100, 100010, 100001, 101010, 101001, 100101

-> dp = [0, 1, 1]
   dp[i] = dp[i-2] + dp[i-1]
"""
n = int(input())
dp = [0, 1, 1]

for i in range(3, n+1):
    dp.append(dp[i-2] + dp[i-1])

print(dp[n])